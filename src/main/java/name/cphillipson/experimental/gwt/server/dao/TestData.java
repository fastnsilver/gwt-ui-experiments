    private List<EnergyOfferDTO> seedEnergyOffers() {
        final List<EnergyOfferDTO> energyOffers = new ArrayList<EnergyOfferDTO>();
        EnergyOfferDTO energyOffer;
        final DateTime startDay = SEVEN_DAYS_AGO_AT_MIDNIGHT.toDateTime();

        for (int m = 0; m <= 1; m++) {
            final String currentMarketType = SCHEDULE_TYPES[m]; // RTBM and DAMKT

            for (int i = 0; i < numberOfResources; i++) {

                final String currentResourceName = getResource(i); // up to 250 resources
                final Boolean currentSlope = RandomUtil.createBoolean();
                DateTime currentDay = startDay;
                // hour between 1-24
                DateTime hour = null;

                for (int k = 1; k <= numberOfDays; k++) { // up to 14 days worth
                    for (int j = 1; j <= numberOfHours; j++) { // up to 24 hours worth
                        energyOffer = new EnergyOfferDTO();

                        hour = currentDay;
                        energyOffer.setDateTime(TimeUtil.dateTimeToIso(hour));

                        energyOffer.setResourceName(currentResourceName);
                        energyOffer.setMarketType(currentMarketType);
                        energyOffer.setSlope(currentSlope);
                        energyOffer.setOfferPriceCurve(seedMwPriceCurve());
                        energyOffers.add(energyOffer);
                        currentDay = currentDay.plusHours(1);
                    }
                }
            }
        }
        return energyOffers;
    }

    private List<ReserveOfferDTO> seedReserveOffers(int productTypeIndex) {
        final List<ReserveOfferDTO> reserveOffers = new ArrayList<ReserveOfferDTO>();
        ReserveOfferDTO reserveOffer;
        final DateTime startDay = SEVEN_DAYS_AGO_AT_MIDNIGHT.toDateTime();

        for (int m = 0; m <= 1; m++) {
            final String currentMarketType = SCHEDULE_TYPES[m]; // RTBM and DAMKT

            for (int i = 0; i < numberOfResources; i++) {

                final String currentResourceName = getResource(i); // up to 250 resources
                DateTime currentDay = startDay;
                // hour between 1-24
                DateTime hour = null;
                for (int k = 1; k <= numberOfDays; k++) { // up to 14 days worth
                    for (int j = 1; j <= numberOfHours; j++) { // up to 24 hours worth
                        reserveOffer = new ReserveOfferDTO();

                        hour = currentDay;
                        reserveOffer.setDateTime(TimeUtil.dateTimeToIso(hour));

                        reserveOffer.setResourceName(currentResourceName);
                        reserveOffer.setMarketType(currentMarketType);
                        reserveOffer.setProductType(PRODUCT_TYPES[productTypeIndex]);
                        reserveOffer.setDispatchStatus(RandomUtil.createString(DISPATCH_STATII));
                        reserveOffer.setFixedMW(BigDecimal.valueOf(POINTS[6].mw));
                        reserveOffer.setPrice(BigDecimal.valueOf(POINTS[6].price));
                        reserveOffers.add(reserveOffer);
                        currentDay = currentDay.plusHours(1);
                    }
                }
            }
        }
        return reserveOffers;
    }

